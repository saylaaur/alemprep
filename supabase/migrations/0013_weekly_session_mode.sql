-- =====================================================
-- 0013: Еженедельные тесты — новое значение enum session_mode
--
-- ЕДИНСТВЕННЫЙ statement в файле: ALTER TYPE ... ADD VALUE нельзя
-- использовать в той же транзакции, где значение ещё используется бы —
-- миграция применяется в отдельной транзакции, значение станет доступно
-- начиная со следующей миграции/коннекта.
-- =====================================================
ALTER TYPE session_mode ADD VALUE IF NOT EXISTS 'weekly';
